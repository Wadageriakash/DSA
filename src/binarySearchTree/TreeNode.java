package binarySearchTree;


 // Represents a node in the Binary Search Tree. Each node has a val (the data), a left child pointer, and a right child pointer.
class TreeNode {
    int val;
    TreeNode left;
    TreeNode right;

    TreeNode(int val) {
        this.val = val;
        this.left = null;
        this.right = null;
    }
}
